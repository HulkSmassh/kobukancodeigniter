							<h3><strong>Shakuhachi:</strong> A Flauta Zen Japonesa</h3>
							<p class="drop-caps">
								A Shakuhachi e uma flauta vertical japonesa feita de bambu que no passado serviu como ferramenta espiritual de monges budistas nas praticas meditativas.
						  </p>
              <p class="drop-caps">
                Ela e derivada de flautas de origem chinesa e teve muitas alterações no correr dos séculos ate adquirir seu formato atual. No principio o instrumento e suas musicas eram de uso da corte. Aos poucos os monges budistas passaram a toca-la também em suas peregrinações em troca de alimentos e oferendas. Assim o shakuhachi acabou se tornando uma ferramenta religiosa mudando do contexto da corte para os templos.
              </p>
              <p class="drop-caps">
                Durante o período Edo muitos samurais do lado derrotado das guerras, os Ronins, tiveram suas espadas confiscadas pelo Xogunato e se transformaram em monges. Porem estes samurais se consideravam de uma classe social mais elevada e não queriam ser rebaixados a monges mendicantes. Eles então se incorporaram a uma seita Zen Budista denomoninada Fuke, que foi criada em 1614 como um ramo da Rinzai Zen. Assim surgiu no Japao uma classe diferenciada de tocadores de Shakuhachi cuja peculiaridade estava no grande chapéu em formato de cesto (tengai) que cobriam completamente suas cabeças durante a perigrinaçao . Foram conhecidos como Komusos, sacerdotes do vazio , e tinham como objetivo a pratica da meditação tocando Shakuhachi para buscar a iluminação através do sopro do instrumento (suizen, sopro Zen). Conforme a lenda os Komusos redesenharam a flauta com um bambu mais longo e resistente para também ser utilizado como arma. De uma peça fina ela foi transformada com a adoçao da raiz do bambu no seu final, o que aumentou seu peso e melhorou consideravelmente sua sonoridade. Alem disto o numero de furos diminuiu de seis para cinco.
              </p>
              <p class="drop-caps">
                Os Komusos receberam passes especiais do Xogunato que lhes permitiam acesso livre a qualquer fronteira do Japao por terra ou agua. Foi então que os espiões e bandidos começaram a se disfarçar com o chapéu de cesto fazendo com que a seita fosse abolida no período Meiji (ate por sua ligação com o Xogunato).
              </p>
              <p class="drop-caps">
                Assim o instrumento acabou voltando as suas origens,ou seja, tendo um sentido mais artístico( Gakki) do que religioso (hoki).
              </p>
              <p class="drop-caps">
                O nome Shakuhachi se refere a um padrão de medida japonês em que o  Shaku,( que equivale a 30,3 cms) ,e adicionado ao sun que e um decimo de shaku. Assim a shakuhachi tem 1 shaku mais 8(hachi) sun que equivale a 54,5 cms nas medidas ocidentais. Embora o nome tenha haver com seu tamanho e possível observar shakuhachis de diferentes tamanhos.
              </p>
              <p class="drop-caps">
                Sua sonoridade ampla e envolvente remete a meditação com seus sons calmos que por vezes imitam animais ou elementos da natureza. O Shakuhachi e capaz de expressar o coração humano em suas mais profundas dimensões.
              </p>
              <p class="drop-caps">
